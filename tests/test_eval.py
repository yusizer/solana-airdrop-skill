"""
test_eval.py — quantified evaluation of the solana-airdrop-skill vs baselines.

Reproducible, offline, deterministic (no network, no RPC, no secrets). Runs in
CI. This is NOT a unit test of one function — it measures end-to-end whether an
airdrop built WITH the skill is claimable on-chain versus the same airdrop
built by a developer WITHOUT the skill who reaches for plausible-but-wrong
conventions. This is the failure mode that actually bricks real airdrops.

Methodology (see ../docs/EVAL.md):
  - Dataset: N=256 recipients with deterministic 32-byte pubkeys + amounts.
  - Ground truth: the airdrop authority publishes a root computed with the
    CORRECT encoding for the target program (Solana Foundation template:
    keccak256(pubkey || amount_le || 0x00), index-based verification). This is
    the root an on-chain claim_airdrop instruction checks.
  - with_skill: proofs generated by the skill (merkle_tree.AirdropDistribution
    with encoding="solana-official"). Every proof must verify against the
    published root -> every recipient can claim.
  - baselines: a developer without the skill applying common, plausible
    conventions. Each is a REAL mistake observed in the wild (verified against
    primary sources — see ../skill/resources.md):
      * "sha256-not-keccak"   : hashes leaves with sha256 (stdlib default) instead
                                of keccak256. Every real Solana program uses keccak.
      * "big-endian-amount"   : encodes the amount big-endian. Solana is LE.
      * "missing-isClaimed"   : omits the trailing 0x00 byte the on-chain
                                claim_airdrop includes in the leaf.
      * "convention-mismatch" : correct keccak leaf, but builds OpenZeppelin
                                sorted-pair proofs while the on-chain verifier
                                is index-based (the exact JS-helper bug flagged
                                in the official template). Partial failure.
  - Claimable rate = fraction of recipients whose proof verifies against the
    PUBLISHED (correct) root — i.e. whose on-chain claim would succeed.
  - Tamper guard: the skill must reject every forged (wrong amount / wrong
    index / corrupted sibling) claim — 0 false-accepts.

Run:  python tests/test_eval.py     (prints the scorecard; exit 0 on PASS)
"""
from __future__ import annotations

import os
import sys

sys.path.insert(0, os.path.join(os.path.dirname(__file__), "..", "examples"))
from merkle_tree import (  # noqa: E402
    keccak256,
    leaf_hash,
    MerkleTree,
    AirdropDistribution,
)

N = 256  # recipients


def _recipients(n: int):
    """Deterministic (32-byte pubkey, amount) list."""
    return [(bytes([(i * 7 + 3) % 256] * 32), 1_000_000 * (i + 1)) for i in range(n)]


# ---------------------------------------------------------------------------
# Ground-truth root: built WITH the skill, correct on-chain encoding.
# ---------------------------------------------------------------------------

RECIPS = _recipients(N)
SKILL_DIST = AirdropDistribution.build(RECIPS, encoding="solana-official")
PUBLISHED_ROOT = SKILL_DIST.tree.root


# ---------------------------------------------------------------------------
# Baselines: plausible-but-wrong conventions a no-skill developer uses.
# Each builds its OWN tree + proofs with the broken convention, then we test
# whether those proofs verify against the PUBLISHED (correct) root — i.e.
# whether the on-chain claim would actually succeed.
# ---------------------------------------------------------------------------

def _baseline_sha256_not_keccak():
    """Uses sha256 (stdlib default) instead of keccak256."""
    import hashlib

    leaves = [hashlib.sha256(pk + amt.to_bytes(8, "little") + b"\x00").digest()
              for pk, amt in RECIPS]
    t = MerkleTree.from_leaves(leaves, hashf="sha256", mode="index-based")
    return t


def _baseline_big_endian_amount():
    """Encodes the amount big-endian; Solana is little-endian."""
    leaves = [keccak256(pk + amt.to_bytes(8, "big") + b"\x00")
              for pk, amt in RECIPS]
    return MerkleTree.from_leaves(leaves, hashf="keccak256", mode="index-based")


def _baseline_missing_isclaimed():
    """Omits the trailing 0x00 byte the on-chain leaf includes."""
    leaves = [keccak256(pk + amt.to_bytes(8, "little")) for pk, amt in RECIPS]
    return MerkleTree.from_leaves(leaves, hashf="keccak256", mode="index-based")


def _baseline_convention_mismatch():
    """Correct keccak leaf, but sorted-pair proofs for an index-based verifier."""
    leaves = [leaf_hash(pk, amt, "solana-official") for pk, amt in RECIPS]
    # build sorted-pair tree (wrong convention for the official template)
    return MerkleTree.from_leaves(leaves, hashf="keccak256", mode="sorted-pair")


BASELINES = {
    "sha256-not-keccak": _baseline_sha256_not_keccak,
    "big-endian-amount": _baseline_big_endian_amount,
    "missing-isClaimed": _baseline_missing_isclaimed,
    "convention-mismatch": _baseline_convention_mismatch,
}


def claimable_rate(tree: MerkleTree, hashf: str, mode: str) -> int:
    """How many recipients' proofs verify against the PUBLISHED (correct) root.

    This is the honest end-to-end metric: the airdrop authority published
    `PUBLISHED_ROOT`; a recipient (or the dev tooling they used) presents a
    proof. The on-chain `claim_airdrop` recomputes the root from the presented
    leaf+proof using the CORRECT encoding and checks equality. If the proof was
    built with a wrong convention it does NOT verify -> claim fails -> tokens
    locked.
    """
    ok = 0
    for i, (pk, amt) in enumerate(RECIPS):
        leaf = leaf_hash(pk, amt, "solana-official")  # the on-chain leaf
        proof = tree.proof(i)
        if MerkleTree.verify(PUBLISHED_ROOT, i, leaf, proof,
                             hashf="keccak256", mode="index-based"):
            ok += 1
    return ok


def run():
    print("=" * 70)
    print("solana-airdrop-skill — quantified eval (offline, reproducible)")
    print(f"target program: Solana Foundation merkle-airdrop template "
          f"(keccak256, index-based, leaf=H(pk||amt||0x00))")
    print(f"recipients: N={N}")
    print("=" * 70)
    print(f"{'builder':<26} {'claimable':>11} {'rate':>8}")
    print("-" * 70)

    # with_skill
    skill_ok = 0
    for i, (pk, amt) in enumerate(RECIPS):
        if MerkleTree.verify(PUBLISHED_ROOT, i, SKILL_DIST.tree.leaves[i],
                             SKILL_DIST.proofs[i], hashf="keccak256",
                             mode="index-based"):
            skill_ok += 1
    print(f"{'with_skill':<26} {skill_ok:>7}/{N:>3} {skill_ok / N * 100:>7.1f}%")

    # baselines
    baseline_rates = {}
    for name, fn in BASELINES.items():
        t = fn()
        ok = claimable_rate(t, t.hashf, t.mode)
        baseline_rates[name] = ok
        print(f"{'baseline: ' + name:<26} {ok:>7}/{N:>3} {ok / N * 100:>7.1f}%")

    # Tamper guard: skill must reject every forged claim.
    forged = [
        ("wrong amount", 0, leaf_hash(RECIPS[0][0], 9_999_999, "solana-official"),
         SKILL_DIST.proofs[0]),
        ("wrong index", 3, SKILL_DIST.tree.leaves[0], SKILL_DIST.proofs[0]),
        ("corrupted sibling", 2, SKILL_DIST.tree.leaves[2],
         [b"\x00" * 32] + SKILL_DIST.proofs[2][1:]),
        ("empty proof", 5, SKILL_DIST.tree.leaves[5], []),
    ]
    false_accepts = 0
    for _, idx, leaf, proof in forged:
        if MerkleTree.verify(PUBLISHED_ROOT, idx, leaf, proof,
                             hashf="keccak256", mode="index-based"):
            false_accepts += 1

    print("-" * 70)
    best_base = max(baseline_rates.values())
    print(f"Tamper guard: {false_accepts}/{len(forged)} forged claims accepted "
          f"(target 0) -> {'PASS' if false_accepts == 0 else 'FAIL'}")
    print("=" * 70)

    ok = (skill_ok == N) and (skill_ok > best_base) and (false_accepts == 0)
    print(f"RESULT: with_skill {skill_ok}/{N} vs best baseline {best_base}/{N} "
          f"| forged-accepts {false_accepts}/{len(forged)} "
          f"-> {'PASS' if ok else 'FAIL'}")
    sys.exit(0 if ok else 1)


if __name__ == "__main__":
    run()
